package sample.model;

public class Produto {
    public int ID;
    public String Nome;
    public String Descricao;
    public int Preco;
    public int Stock;

    public Produto(int ID, String Name, String Description, int Price, int Stock){
        this.ID=ID;
        this.Nome=Name;
        this.Descricao=Description;
        this.Preco=Price;
        this.Stock=Stock;
}}
